    private void btnGuardarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarVentaActionPerformed
        // TODO add your handling code here:
        String codigo = txtNombre.getText();
        String nombre = txtCodProducto.getText();
        int precio = Integer.parseInt(txtPrecio.getText());
        int cantidad= Integer.parseInt(txtCantidad.getText());
        String descripcion = txtDescripcion.getText();
        String categoria = txtCategoria.getText();
        String CodVenta = txtCodVenta.getText();
        String fecha = txtFechaVenta.getText();
        int unidades = Integer.parseInt(txtUnidades.getText());
        
        Articulo art = new Articulo(codigo, nombre, precio, cantidad, descripcion, categoria);
        Venta venta = new Venta(CodVenta, fecha, unidades, art);
        int total = venta.calcularTotal();
        venta.setTotalPago(total);
        if(controGestion.guardarVenta(venta)){
            JOptionPane.showMessageDialog(null, "Se guardo");
            limpiar();
        }else{
            JOptionPane.showMessageDialog(null, "No se guardo");
        }
    }//GEN-LAST:event_btnGuardarVentaActionPerformed

